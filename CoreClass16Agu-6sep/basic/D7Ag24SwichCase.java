package com.rays.classs.basic;

public class D7Ag24SwichCase {
	public static void main(String[] args) {

		char c = '@';
		
		switch (c) {
		case 'a':
			System.out.println("a is vovel");
			break;
		case 'e':
			System.out.println("e is vovel");
			break;
		case 'i':
			System.out.println("i is vovel");
			break;
		case 'o':
			System.out.println("o is vovel");
			break;
		case 'u':
			System.out.println("u is vovel");
			break;
			default:
			System.out.println("No vovel");break;
		}
	}
}
